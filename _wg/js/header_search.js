//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 검색창 클릭시 사이즈 변경
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
$(function () {
	$(".header_search").click(function () {
		$(".header_search").addClass("long");
	});
	$("body").click(function (e) {
	if ($(".header_search").css("display") != "none") {
		if (!$(".header_search").has(e.target).length) {
			$(".header_search").removeClass("long");
			}
		}
	});
});